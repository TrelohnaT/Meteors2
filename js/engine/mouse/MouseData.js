export default class MouseData {
    constructor(x, y, leftBtn, middleBtn, rightBtn) {
        this.x = 0;
        this.y = 0;
        this.leftBtn = false;
        this.middleBtn = false;
        this.rightBtn = false;
        this.x = x;
        this.y = y;
        this.leftBtn = leftBtn;
        this.middleBtn = middleBtn;
        this.rightBtn = rightBtn;
    }
}
